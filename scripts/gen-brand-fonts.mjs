// Regenerates src/templates/brand-fonts.ts: downloads the Latin (U+0000-00FF)
// woff2 subsets of the brand substitute fonts from Google Fonts and inlines
// them as base64 @font-face rules so the brand templates render offline.
// Run: bun scripts/gen-brand-fonts.mjs
import { writeFileSync } from "fs";

const UA =
  "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0 Safari/537.36";
const CSS_URL =
  "https://fonts.googleapis.com/css2?family=Hanken+Grotesk:wght@400..700&family=Inter:wght@400..800&family=Playfair+Display:ital,wght@0,700..800;1,700&family=Source+Serif+4:opsz,wght@8..60,400..600&display=swap";

const css = await (await fetch(CSS_URL, { headers: { "User-Agent": UA } })).text();
const blocks = css.split("@font-face").slice(1);

const faces = [];
let total = 0;
for (const b of blocks) {
  const ur = (b.match(/unicode-range:\s*([^;]+);/) || [])[1] || "";
  if (!ur.includes("U+0000-00FF")) continue; // Latin block only; CJK falls back
  const fam = (b.match(/font-family:\s*'([^']+)'/) || [])[1];
  const style = (b.match(/font-style:\s*([^;]+);/) || [])[1].trim();
  const weight = (b.match(/font-weight:\s*([^;]+);/) || [])[1].trim();
  const url = (b.match(/src:\s*url\(([^)]+)\)/) || [])[1];

  const buf = Buffer.from(await (await fetch(url)).arrayBuffer());
  total += buf.length;
  faces.push(
    `@font-face{font-family:'${fam}';font-style:${style};font-weight:${weight};font-display:swap;` +
      `src:url(data:font/woff2;base64,${buf.toString("base64")}) format('woff2');unicode-range:${ur.trim()};}`
  );
  console.error(`${fam} ${style} ${weight}: ${(buf.length / 1024).toFixed(1)}KB`);
}

const out =
  `// AUTO-GENERATED by scripts/gen-brand-fonts.mjs — do not edit by hand.\n` +
  `// Bundled Latin (U+0000-00FF) woff2 subsets of the brand substitute fonts so\n` +
  `// Apple/Notion/Lovable/Wired render offline. CJK still falls back to system fonts.\n` +
  `export const BRAND_FONT_CSS = ${JSON.stringify(faces.join("\n"))};\n`;
writeFileSync("src/templates/brand-fonts.ts", out);
console.error(`\nTOTAL woff2: ${(total / 1024).toFixed(1)}KB | brand-fonts.ts: ${(out.length / 1024).toFixed(1)}KB | faces: ${faces.length}`);
